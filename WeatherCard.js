import React from "react";

function WeatherCard({ weather }) {
  if (!weather) return null;

  let alertClass = "safe";
  if (weather.temperature > 35 || weather.windspeed > 50) alertClass = "emergency";
  else if (weather.temperature > 30 || weather.windspeed > 30) alertClass = "caution";

  return (
    <div className="weather-card">
      <h3><i className="fa-solid fa-cloud-sun"></i> Current Weather</h3>
      <p>Temperature: {weather.temperature}Â°C</p>
      <p>Wind Speed: {weather.windspeed} km/h</p>
      <p className={alertClass}>
        Alert Level: {alertClass.toUpperCase()}
      </p>
    </div>
  );
}

